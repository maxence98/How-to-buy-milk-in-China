// functions that emphasizes/de-emphasizes the pie charts
function emphasize(option, chart) {
	option.title.textStyle = {
      fontWeight: 'bold',
      fontFamily: 'Open Sans',
      fontSize: 18,
      color: 'red',
  }
  option.series[0].radius = 70
  chart.setOption(option)
}

function de_emphasize(option, chart) {
	option.title.textStyle = {
      fontWeight: 400,
      fontFamily: 'Open Sans',
      fontSize: 18,
      color: 'black'
  }
  option.series[0].radius = 50
  chart.setOption(option)
}