// function that changes the y axis (nutrition)
function changeNutrition(nutrition) {
  if (nutrition == 'protein') {
    option_pri_nut.series[0].encode.y = 'protein'
    option_pri_nut.yAxis.name = 'Nutrition Fact: Protein (g/100mL)'
    option_pri_nut.yAxis.max = 4.2
    option_pri_nut.yAxis.min = 2.7
  } else {
    option_pri_nut.series[0].encode.y = 'calcium'
    option_pri_nut.yAxis.name = 'Nutrition Fact: Calcium (mg/100mL)'
    option_pri_nut.yAxis.max = 135
    option_pri_nut.yAxis.min = 95
  }
  pri_nut.setOption(option_pri_nut);
}

